with app.app_context():
    Novel.query.filter(Novel.id == 10).delete()
    db.session.commit()

    n = Novel(category='科幻', user_idea='程序员穿越赛博朋克世界，用代码对抗AI统治',
              word_count=500000, status='draft')
    db.session.add(n)
    db.session.flush()

    n.title = '赛博代码'

    for vi, (vol_title, vol_info) in enumerate(CHAPTERS.items()):
        v = Volume(novel_id=n.id, order=vi + 1, title=vol_title, description=vol_info['desc'])
        db.session.add(v)
        db.session.flush()
        for ci, (ch_title, paragraphs) in enumerate(vol_info['chapters']):
            content = '\n\n'.join(f'　　{p}' for p in paragraphs)
            c = Chapter(volume_id=v.id, order=ci + 1, title=ch_title, outline=paragraphs[0], content=content)
            db.session.add(c)

    db.session.commit()
    print(f'seed id={n.id} 9 chapters with unique content')
